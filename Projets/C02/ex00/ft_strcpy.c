#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int		main(void)
{
	char *chaine;
	char reserve[30];

	chaine = "copier moi ici";
	printf("%s\n", ft_strcpy(reserve, chaine));
	return (0);
}
